#include <stdint.h>

/* MASKS PLL */
#define PLLM_MASK  (0x3F << 0)
#define PLLN_MASK  (0x1FF << 6)
#define PLLP_MASK  (0x3 << 16)

int main(void)
{
    /* RCC */
    volatile uint32_t *RCC_CR       = (uint32_t*) 0x40023800;
    volatile uint32_t *RCC_PLLCFGR  = (uint32_t*) 0x40023804;
    volatile uint32_t *RCC_CFGR     = (uint32_t*) 0x40023808;
    volatile uint32_t *RCC_AHB1ENR  = (uint32_t*) 0x40023830;

    /* FLASH */
    volatile uint32_t *FLASH_ACR    = (uint32_t*) 0x40023C00;

    /* GPIOA */
    volatile uint32_t *GPIOA_MODER  = (uint32_t*) 0x40020000;

    /* GPIOD */
    volatile uint32_t *GPIOD_MODER  = (uint32_t*) 0x40020C00;
    volatile uint32_t *GPIOD_ODR    = (uint32_t*) 0x40020C14;

    /* =========================================================
       1. ACTIVAR HSE
       ========================================================= */
    *RCC_CR |= (1 << 16);

    while(!(*RCC_CR & (1 << 17)));

    /* =========================================================
       2. CONFIGURAR FLASH PARA 168 MHz
       ========================================================= */
    *FLASH_ACR &= ~(7 << 0);
    *FLASH_ACR |=  (5 << 0);

    /* =========================================================
       3. CONFIGURAR PLL
          HSE = 8 MHz

          VCO_IN  = 8 / 8   = 1 MHz
          VCO_OUT = 1 * 336 = 336 MHz
          PLLCLK  = 336 / 2 = 168 MHz
       ========================================================= */

    *RCC_PLLCFGR &= ~(PLLM_MASK | PLLN_MASK | PLLP_MASK);

    *RCC_PLLCFGR |=
            (1 << 22) |   // PLL source = HSE
            (8 << 0)  |   // PLLM = 8
            (336 << 6)|   // PLLN = 336
            (0 << 16);    // PLLP = 2

    /* =========================================================
       4. ACTIVAR PLL
       ========================================================= */
    *RCC_CR |= (1 << 24);

    while(!(*RCC_CR & (1 << 25)));

    /* =========================================================
       5. SYSCLK = PLL
       ========================================================= */
    *RCC_CFGR &= ~(3 << 0);
    *RCC_CFGR |=  (2 << 0);

    while(((*RCC_CFGR >> 2) & 3) != 2);

    /* =========================================================
       6. ACTIVAR GPIOA Y GPIOD
       ========================================================= */
    *RCC_AHB1ENR |= (1 << 0);
    *RCC_AHB1ENR |= (1 << 3);

    /* =========================================================
       7. PA8 = ALTERNATE FUNCTION (MCO1)
       ========================================================= */
    *GPIOA_MODER &= ~(3 << 16);
    *GPIOA_MODER |=  (2 << 16);

    /* =========================================================
       8. MCO1 = PLL
          PRESCALER = /5

          168 MHz / 5 = 33.6 MHz
       ========================================================= */
    *RCC_CFGR &= ~((3 << 21) | (7 << 24));

    *RCC_CFGR |=
            (2 << 21) |   // MCO1 source = PLL
            (0 << 24);    // MCO1 prescaler = /5

    /* =========================================================
       9. PD12 OUTPUT (LED)
       ========================================================= */
    *GPIOD_MODER &= ~(3 << 24);
    *GPIOD_MODER |=  (1 << 24);

    /* =========================================================
       LOOP
       ========================================================= */
    while(1)
    {
        *GPIOD_ODR ^= (1 << 12);

        for(volatile uint32_t i = 0; i < 1000000; i++);
    }
}
